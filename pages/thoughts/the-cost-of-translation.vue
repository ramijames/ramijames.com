      <p>
        Product stands out as an odd duck. It fits neither business nor design nor engineering cleanly.
        It exists as a translation layer—and organizations would be better off without it.
      </p>

      <h3>Updating roles to fit modern needs</h3>

      <p>
        Instead of PMs and POs dictating feature sets, they should be removed entirely. In their place:
        a working pair of Product Designer and Product Engineer who directly interface with stakeholders,
        interpret needs, create proposals, and execute.
      </p>

