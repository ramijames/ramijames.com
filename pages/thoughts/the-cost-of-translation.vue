      <h2>Introduction</h2>

      <p>
        For those of us who have been working in tech for a long time (1996 for me), there are often common
        themes that show up from organization to organization. Companies are, after all, just made up of
        people, and people will be people.
      </p>

      <p>
        Today I wanted to talk a little bit about inefficiency in product development. I’ve seen a lot of
        companies try to make software. Lots of failures, a few successes. Some of those failures came about
        at the market level. Just a bad fit or wrong timing. You know the drill.
      </p>

      <p>
        Other times, those failures happen before the product even hits the public. Doomed from the start,
        you could say.
      </p>

      <p>
        I think that most of these latter instances of failure do not come from poor execution, but rather
        from the act of translation. Translation between intent and requirements, between insight and
        roadmap, between user need and business constraint.
      </p>

      <p>
        Any way that an organization can reduce translation in their processes, there will be a resulting
        improvement in business needs achievement, speed of development, and reduced time to market so
        that critical user feedback can be acquired.
      </p>

      <p><strong>You have to cut out the fat if you want a tasty product.</strong></p>
      
      <p>Let’s talk about that fat.</p>

      <h2>The cost of translation</h2>

      <h3>Every handoff introduces loss, delay, and distortion</h3>

      <p>
        I think that we can frame this as a basic organizational physics problem. Every step along the way
        requires some interpretation, re-framing, and justification. These things all have an inherent risk
        of introducing problems in a group of people where nobody wants to take ownership (or blame!).
      </p>

      <p>
        Modern Agile groups tend to have many layers of translation because there are many different people
        involved. There’s a kind of insane set of meetings that tends to happen where each group, according
        to its place in the hierarchy, tries to define what will be in the product and how it will work.
      </p>

      <img src="/articles/typical.png" alt="The typical agile nonsense" />

      <p>
        Each step through the chain is essentially a game of telephone. Misunderstanding,
        reinterpretation, and incompetence dilute or mangle the original intent. The meeting structures
        required to make this “work” amount to self-inflicted insanity.
      </p>

      <h2>Solving the right problems</h2>

      <p>
        The core issue with information flow has to do with silos and access. BizDev teams know one set of
        information, designers another, and engineers yet another. Each group, operating alone, would
        likely build a different product.
      </p>

      <p>
        A company must meld these silos into a single, coherent whole that can be released to market.
      </p>

