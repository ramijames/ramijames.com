      <p>Most experienced teams have worked with designers or product professionals before. It doesn’t always go well, for a myriad of reasons. I believe that what makes users love a product stems from a consistent vision born from a partner who is there for the long haul.</p>

      <p>Often, freelancers attempt to deliver what’s in the spec sheet and then move on to the next project. Afterall, they are there to get paid. Sometimes the output hits the target on the first go. More often than not, it misses something subtle and important, and the implementing or business team doesn't realize it until months later. By that time, the context has shifted, the thread has been lost, and there is a substantial ramp-up cost associated with starting anew.</p>

